        /// Extract follow-up questions from AI response content
        /// </summary>
        private static List<string> ExtractNextQuestions(string content)
        {
            var questions = new List<string>();
            var sentences = content.Split('.', '?', '!');

            foreach (var sentence in sentences)
            {
