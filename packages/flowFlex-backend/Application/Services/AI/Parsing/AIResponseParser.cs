                        var missingStage = stages[results.Count];
                        results.Add(GenerateFallbackQuestionnaire(missingStage));
                    }

                    return results;
                }
            }
            catch (Exception ex)
            {
                _logger.LogWarning(ex, "Failed to parse batch questionnaire response");
            }

            return null;
        }

