        /// <inheritdoc />
        public string FixJsonContent(string jsonContent)
        {
            if (string.IsNullOrEmpty(jsonContent))
                return jsonContent;

            var result = jsonContent;

            try
            {
                // Fix unquoted property names in objects (e.g., {id: "1"} -> {"id": "1"})
                result = Regex.Replace(
                    result,
                    @"(?<=[{,]\s*)([a-zA-Z_][a-zA-Z0-9_]*)\s*:",
                    "\"$1\":");

                // Fix single quotes to double quotes
                result = result.Replace("'", "\"");

                // Fix trailing commas before closing brackets
                result = Regex.Replace(result, @",\s*}", "}");
                result = Regex.Replace(result, @",\s*\]", "]");

                // Fix missing commas between array elements (common AI mistake)
                // e.g., }{ -> },{
                result = Regex.Replace(result, @"}\s*{", "},{");

                _logger.LogDebug("JSON content fixed");
            }
            catch (Exception ex)
            {
                _logger.LogWarning(ex, "Failed to fix JSON content, using original");
            }

            return result;
        }

        /// <inheritdoc />
        public List<QuestionOptionDto> ParseQuestionOptions(List<string> options, int questionIndex)
        {
            if (options == null || !options.Any())
                return new List<QuestionOptionDto>();

            var result = new List<QuestionOptionDto>();
            var optionIndex = 1;

            foreach (var opt in options)
            {
                if (string.IsNullOrEmpty(opt))
                    continue;

                try
                {
                    // Try to parse as JSON object first
                    if (opt.TrimStart().StartsWith("{"))
                    {
                        var optionObj = JsonSerializer.Deserialize<JsonElement>(opt);
                        var id = optionObj.TryGetProperty("id", out var idEl) ? idEl.GetString() : optionIndex.ToString();
                        var value = optionObj.TryGetProperty("value", out var valEl) ? valEl.GetString() : opt;
                        var label = optionObj.TryGetProperty("label", out var labelEl) ? labelEl.GetString() : value;

                        result.Add(new QuestionOptionDto
                        {
                            Id = id,
                            Label = label,
                            Value = value,
                            Order = optionIndex
                        });
                    }
                    else
                    {
                        // Plain string format
                        result.Add(new QuestionOptionDto
                        {
                            Id = optionIndex.ToString(),
                            Label = opt,
                            Value = opt,
                            Order = optionIndex
                        });
                    }
                }
                catch (Exception ex)
                {
                    // Fallback to plain string, log for debugging
                    _logger.LogDebug(ex, "Failed to parse option");
                    result.Add(new QuestionOptionDto
                    {
                        Id = optionIndex.ToString(),
                        Label = opt,
                        Value = opt,
                        Order = optionIndex
                    });
                }

                optionIndex++;
            }

            return result;
        }

        /// <summary>
        /// Determine the effective language for summary generation based on input content
        /// </summary>
        /// <param name="input">Stage summary input</param>
        /// <returns>Language code (e.g., "zh-CN" or "en-US")</returns>
        public static string DetermineEffectiveLanguage(AIStageSummaryInput input)
        {
            var lang = input.Language?.Trim();
            if (!string.IsNullOrWhiteSpace(lang) && !string.Equals(lang, "auto", StringComparison.OrdinalIgnoreCase))
            {
                return lang;
            }

            // Collect text content for heuristic detection
            var sb = new StringBuilder();
            if (!string.IsNullOrWhiteSpace(input.StageName)) sb.Append(input.StageName);
            if (!string.IsNullOrWhiteSpace(input.StageDescription)) sb.Append(input.StageDescription);
            if (input.ChecklistTasks != null)
            {
                foreach (var t in input.ChecklistTasks)
                {
                    if (!string.IsNullOrWhiteSpace(t.TaskName)) sb.Append(t.TaskName);
                    if (!string.IsNullOrWhiteSpace(t.Description)) sb.Append(t.Description);
                    if (!string.IsNullOrWhiteSpace(t.CompletionNotes)) sb.Append(t.CompletionNotes);
                }
            }
            if (input.QuestionnaireQuestions != null)
            {
                foreach (var q in input.QuestionnaireQuestions)
                {
                    if (!string.IsNullOrWhiteSpace(q.QuestionText)) sb.Append(q.QuestionText);
                    if (q.Answer != null) sb.Append(q.Answer?.ToString());
                }
            }

            var combined = sb.ToString();
            return ContainsCjk(combined) ? "zh-CN" : "en-US";
        }

        /// <summary>
        /// Detect if a string contains CJK (Chinese) characters
        /// </summary>
        /// <param name="text">Text to check</param>
        /// <returns>True if the text contains CJK characters</returns>
        public static bool ContainsCjk(string text)
        {
            if (string.IsNullOrEmpty(text)) return false;
            // Basic CJK Unified Ideographs range
            return Regex.IsMatch(text, "[\\u4e00-\\u9fff]");
        }

